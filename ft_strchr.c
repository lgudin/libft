#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != ((char)c) && s[i])
		i++;
	if ((((char)c) == '\0' && ((char)c) == '\0') || ((char)c) == ((char *)s)[i])
	{
		return (&((char *)s)[i]);
	}
	return (NULL);
}
