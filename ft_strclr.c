#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	len;
	size_t	i;

	if (s)
	{
		len = ft_strlen(s);
		i = 0;
		while (i < len)
		{
			s[i] = '\0';
			i++;
		}
	}
}
