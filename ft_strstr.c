#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int i;
	int y;

	i = 0;
	if (*s2 == 0)
		return (((char*)s1));
	while (s1[i])
	{
		y = 0;
		if (s1[i] == s2[0])
		{
			while (s1[i + y] == s2[y] && s1[i + y] != '\0' && s2[y] != '\0')
				y++;
			if (s2[y] == '\0')
				return (&((char *)s1)[i]);
		}
		i++;
	}
	return (0);
}
